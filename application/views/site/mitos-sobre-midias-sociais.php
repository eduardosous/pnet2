				<p>Lembre-se que tudo o que é postado em sua página tem visibilidade pública. Apagar um comentário negativo poderá revoltar o usuário, que comentará mais uma vez e, em alguns casos, postará o print screen do comentário apagado.</p>
				
				<p>Se você deseja que sua marca mantenha uma boa reputação nas mídias sociais, aprenda a lidar com o feedback negativo. Não o ignore, principalmente, não o exclua.</p>
				
