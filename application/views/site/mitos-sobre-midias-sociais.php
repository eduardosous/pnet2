				<p>Pode parecer que, quanto mais conteúdo for compartilhado, mais pessoas serão alcançadas. Acontece que há dois tipos de feed de notícias nas mídias sociais: um é atualizado por posts recentes (Twitter, Instagram) e outro é atualizado por posts relevantes (Facebook).</p>
				
				<p>No primeiro caso, postar muito conteúdo poderá chatear os fãs. Só porque eles gostam da sua marca não significa que eles consumirão toda a informação que você publica. É preciso maneirar e focar na qualidade para que os posts não sejam publicados sem gerar resultados.</p>
				
				<p>No segundo caso, não adianta publicar com muita frequência se o fundamento utilizado para aparecer na timeline dos usuários é a relevância. No Facebook, os posts aparecem na timeline de acordo com a preferência dos usuários. Quanto mais você curte os posts de uma página, mais chances de você receber suas próximas atualizações.</p>
				
