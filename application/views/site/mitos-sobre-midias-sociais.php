				<h3>Mito #4 - Preciso estar em todas as mídias sociais</h3>
				
				<p>É importante saber que a forma como sua empresa utiliza uma determinada mídia social não necessariamente funcionará, se usada da mesma maneira, em outra. Cada mídia social possui diferenças simples como faixa etária média, objetivo e tipo de mídia predominante compartilhada pelos usuários.  </p>
				
