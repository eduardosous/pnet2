				<p>Talvez seja a métrica mais popular das mídias sociais, mas nem de longe é a mais importante. Não que não seja importante, apenas não é a principal.</p>
				
				<p>O número de fãs ou seguidores que você possui é uma prova social de que sua empresa é engajada socialmente e pode influenciar decisões. Mas esses fãs podem não fazer parte o seu público-alvo e não serem potenciais consumidores da sua marca.</p> 
				
				<p>Defina quem será o público que seguirá sua marca nas mídias sociais e trabalhe para que suas publicações cheguem a ele. Ter no radar a audiência errada pode custar mais caro no final.</p>
				
				<h3>Mito #2 - Use o maior número de hashtags possível</h3>
				
				<p>O uso de hashtags (#) é uma prática importante nas mídias sociais. Funciona tanto para engajar a audiência exata como para atingir pessoas que ainda não seguem sua página. Porém, elas devem ser usadas com cautela.</p>
				
				<p>Publicações com mais de 10 hashtags (ou até menos) têm uma péssima aparência e não costumam ser levadas a sério pelas pessoas por serem facilmente confundidas com spam.</p>
				
