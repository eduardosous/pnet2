            <h2>Mitos sobre Produção de Conteúdo – Blog Plataformanet</h2>
                
                <p>Quando estudamos Marketing Digital e procuramos quais são as boas práticas em Produção de Conteúdo, é comum nos depararmos com "verdades" que são ditas por diferentes fontes. </p>
				
				<p>Isso nos faz assumir essas proposições como sendo verdadeiras, sem entender o contexto ou a forma como essas estratégias foram aplicadas.</p> 

				<p>Por isso, neste post desmitificamos alguns mitos, explicando quais práticas devem ser evitadas e como aplicar as estratégias do Marketing de Conteúdo em sua empresa.</p>

				<h3>Mito #1 - Marketing de Conteúdo é sobre a minha empresa</h3>

				<p>O primeiro mito é, possivelmente, o erro mais primário que as empresas que estão começando a aplicar a metodologia cometem. Marketing de Conteúdo não é sobre a sua empresa, mas sobre seu público.</p>

				<p>Diferentemente de uma assessoria de imprensa, que, aí sim, é sobre sua empresa, Marketing de Conteúdo é sobre os problemas/dúvidas/curiosidades que seu público enfrenta e as informações e o conhecimento que você possui para resolvê-los.</p>

				<p>As boas práticas de Marketing de Conteúdo preveem que a metodologia funciona para educar e fidelizar seu público-alvo sobre o assunto de seu nicho de mercado. O intuito disso é fazer com que esse conteúdo gere credibilidade e influencie na decisão de compra.</p>

				<p>Em outras palavras, é para pessoas que poderão futuramente fazer parte de sua cartela de clientes. É fazer as pessoas conhecerem sua empresa antes de oferecer o produto/serviço.</p>

				<h3>Mito #2 - Marketing de Conteúdo só existe na internet</h3>

				<p>Apesar de ser uma prática relacionada ao Inbound Marketing, que, aí sim, só é possível por meio da internet, a origem do marketing de conteúdo data de antes do desenvolvimento da rede mundial de computadores.</p>

				<p>Qualquer informação produzida por uma marca, mas que tenha como prioridade passar informação relevante ao invés de fazer publicidade, pode ser considerada marketing de conteúdo.</p>

				<h3>Mito #3 - Fazer marketing de conteúdo é criar um blog</h3>

				<p>Um blog é um canal importantíssimo em uma estratégia de marketing de conteúdo e é considerado peça fundamental. No entanto, o marketing de conteúdo vai além do blog. É possível produzir conteúdo para vários outros canais. E de várias outras maneiras além de posts.</p>

				<p>O marketing de conteúdo também abrange materiais ricos como eBooks, webinars, templates, hangouts e outras formas de conteúdo. Além do mais, existem milhares de blogs na internet que não se enquadram como marketing de conteúdo, como blogs pessoais ou de notícias.</p>

				<h3>Mito #4 - Preciso publicar todos os dias no blog, pois quanto mais conteúdo, mais resultados!</h3>

				<p>A publicação diária não é o principal fator que vai determinar o sucesso de uma estratégia de marketing de conteúdo, mas sim a publicação periódica.</p>

				<p>O marketing de conteúdo vai além da simples produção de conteúdo, não é “quanto mais você produz, mais resultados você tem”. Manter uma periodicidade definida é muito mais importante. Ter um conteúdo de alta relevância e qualidade, aumenta as suas chances de ser encontrado nos mecanismos de busca.</p>

				<p>É pior ter um monte de conteúdo de qualidade duvidosa, do que ter uma porção de material mais selecionado, mas de altíssima qualidade. Quantidade é bom, mas a qualidade é o principal fator que vai determinar o sucesso de sua estratégia de conteúdo.</p>

				<h3>Mito #5 - Preciso de um assessor de imprensa para fazer marketing de conteúdo</h3>

				<p>Essa é outra inverdade bastante comum, pois não cabe à função de uma assessoria produzir conteúdo para sua empresa. A assessoria é uma área que possui funções como a criação de releases e o relacionamento com a mídia. Um trabalho bem diferente de um produtor de conteúdo.</p>
				
				<p>É claro que isso não impede que um assessor de imprensa produza conteúdo, apenas os métodos são diferentes. Como dito no mito 1, marketing de conteúdo é sobre seus clientes e não sobre sua empresa. O trabalho de assessoria de imprensa é bastante importante, mas é outro trabalho.</p>

				<h3>Mito #6 - Falar sobre o que eu sei vai matar meu negócio e abastecer meus concorrentes</h3>

				<p>Esse mito é um medo comum para a maioria das pessoas que fazem marketing de conteúdo. Em uma primeira visão, realmente parece que revelar os segredos e o conhecimento de graça irá matar seu negócio, pois as pessoas irão fazer tudo sozinhas e não precisarão de sua ajuda.</p>

				<p>Mas, observando por uma ótica um pouco mais profunda, o que acontece na verdade é que, quando você revela seus segredos e entrega todo seu conhecimento de forma gratuita, você acaba criando identificação e respeito com seu público e tornando-se referência no assunto. Isso é um fator importantíssimo e que influencia fortemente na decisão de compra do cliente.</p>
				
				<h3>Mito #7 - Você deve republicar o mesmo conteúdo em diferentes canais</h3>

				<p>Em um primeiro momento parece que, quanto mais você replicar o seu conteúdo em diferentes canais melhor será o resultado. Acontece que isso não é verdade. Como dito no mito 4, o Google valoriza conteúdo único e autoral. Isso não significa que você deva apenas escolher um canal e deixar os outros de lado. O mais inteligente é traçar diferentes estratégias para cada tipo de postagem.</p>

				<p>Mas, então, por que publicar conteúdo único em outros canais? Há vários motivos: criar um relacionamento com outra audiência, fortalecer a parceria com outros blogs, aprimorar a estratégia de SEO off page com link building em outros sites, dentre outros.</p>
				
				<h3>Mito #8 - Marketing de conteúdo é Inbound Marketing</h3>

				<p>Apesar de serem duas metodologias que se complementam, não significa que sejam sinônimos. Marketing de conteúdo está dentro de um conceito de Inbound Marketing, que por sua vez precisa do conteúdo para gerar valor em, no mínimo, uma das etapas do Inbound.</p>
				
				<p>Conteúdo pode ser tanto para atração (para chamar a persona para seu site) como para retenção (para manter o cliente fidelizado por meio de conteúdo gratuito e de qualidade).</p>

				<p>Enfim, podemos ver que ainda existem muitos mitos a serem desvendados sobre a produção de conteúdo. Por isso, é importante interpretar essas dicas e entender se fazem ou não sentido para o seu contexto.</p>

				<p>Em linhas gerais, é sempre importante acompanhar e procurar por casos de sucesso e ter por perto especialistas para tirar as dúvidas e entender o que é aplicável ao seu negócio.</p>

